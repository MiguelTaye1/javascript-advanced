// compund assignment operators
let x = 10;

x += 2; // x = x + 2
console.log("After += 2, x is:", x); // 12

x *= 3; // x = x * 3
console.log("After *= 3, x is:", x); // 36

//Increment and Decrement Operators
let y = 1;

y++;
console.log("After incrementing, y is:", y); // 2
++y; // This is a unary plus operator, it does not change the value of y
console.log("After another increment, y is:", y); // 2

